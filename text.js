function kerning(chr1, chr2) {
	if (chr1 == ' ' || chr2 == ' ')
		return ' ';
	for (var i = 0; i < 5; i++) {
		if (ls[chr1][i].charAt(ls[chr1][i].length-1) == '#' && (
				(i != 0 && ls[chr2][i-1].charAt(0) == '#') ||
				(ls[chr2][i].charAt(0) == '#') ||
				(i != 4 && ls[chr2][i+1].charAt(0) == '#')
			)) {
			return ' ';
		}
	}
	return '';
}